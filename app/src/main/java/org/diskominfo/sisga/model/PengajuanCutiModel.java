package org.diskominfo.sisga.model;

public class PengajuanCutiModel {
    String judul, desc;

    public PengajuanCutiModel(String judul, String desc) {
        this.judul = judul;
        this.desc = desc;
    }

    public String getJudul() {
        return judul;
    }

    public String getDesc() {
        return desc;
    }
}
